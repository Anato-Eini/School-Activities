CREATE TABLE Posts (
    post_id INT PRIMARY KEY,
    title VARCHAR(100),
    content TEXT,
    user_id INT
);

CREATE TABLE Comments (
    comment_id INT PRIMARY KEY,
    post_id INT,
    user_id INT,
    comment_text TEXT,
    FOREIGN KEY (post_id) REFERENCES Posts(post_id)
);

